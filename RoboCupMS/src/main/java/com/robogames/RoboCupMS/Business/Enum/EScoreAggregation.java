package com.robogames.RoboCupMS.Business.Enum;

/**
 * Agregacni funkce pro skore (uplatnuje se ve vyhodnocovani)
 */
public enum EScoreAggregation {

    /**
     * Minimalni skore ze vsech zapasu odehranych robotem
     */
    MIN,

    /**
     * Maximalni skore ze vsech zapasu odehranych robotem
     */
    MAX,

    /**
     * Soucet skore vsech zapasu odehranych robotem
     */
    SUM;

}
